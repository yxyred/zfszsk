package com.css.common.enums;

public class DicCodeBean {

    public final String code;

    public final String text;

    public DicCodeBean(String code, String text) {
        this.code = code;
        this.text = text;
    }

    public String getCode() {
        return code;
    }

    public String getText() {
        return text;
    }
}
